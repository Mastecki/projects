# Escape character      Prints As

# \'                    '
print('those are bob\'s.')

# \"                    "
print('poland is \"strong\"')

# \t                    tab
print('gimme some huge \t space')

# \n                    newline(line break)
print('hello there.\nhow are you?')

# \\                    \
print('you should use / to type path, not \\')

# """ xxx """           multistring
print("""ggg
hhh
jjj
kkk
lll""")

spam = 'hello world'
print(spam[0])
print(spam[0:2])
print(spam[-1])
